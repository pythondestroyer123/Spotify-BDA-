    private void btnRegistrarseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarseActionPerformed
        String nombre = txtNombre.getText().trim();
    String correo = txtCorreo.getText().trim();
    String telefono = txtTelefono.getText().trim();
    String contrasena = new String(pswContrasena.getPassword()).trim();

    if (nombre.isEmpty() || correo.isEmpty() || telefono.isEmpty() || contrasena.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Por favor complete todos los campos.");
        return;
    }

    if (existeCorreo(correo)) {
        JOptionPane.showMessageDialog(this, "❌ El correo ya está registrado.\n Intente nuevamente ingresando un correo diferente.");
        return;
    }

    if (existeContrasena(contrasena)) {
        JOptionPane.showMessageDialog(this, "❌ La contraseña ya está registrada.\n Intente nuevamente ingresando una contrasena diferente.");
        return;
    }

    if (existeTelefono(telefono)) {
        JOptionPane.showMessageDialog(this, "❌ El teléfono ya está registrado.\n Intente nuevamente ingresando un telefono diferente.");
        return;
    }

    Paciente nuevo = new Paciente(nombre, correo, contrasena, telefono);
    listaPacientes.add(nuevo);
    JOptionPane.showMessageDialog(this, "✅ Paciente registrado exitosamente.");
    
    txtNombre.setText("");
    txtCorreo.setText("");
    txtTelefono.setText("");
    pswContrasena.setText("");
    }//GEN-LAST:event_btnRegistrarseActionPerformed

  
