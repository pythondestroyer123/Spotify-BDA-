    private void btnUsuariosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUsuariosActionPerformed
         if (listaPacientes.isEmpty()) {
        JOptionPane.showMessageDialog(this, "No hay pacientes registrados.");
        return;
    }

    StringBuilder sb = new StringBuilder("Pacientes Registrados:\n\n");
    for (Paciente p : listaPacientes) {
        sb.append("- ").append(p.nombre).append("\n");
    }

    JOptionPane.showMessageDialog(this, sb.toString());
    }//GEN-LAST:event_btnUsuariosActionPerformed

    
      private boolean existeCorreo(String correo) {
        for (Paciente p : listaPacientes) {
            if (p.correo.equalsIgnoreCase(correo)) {
                return true;
            }
        }
        return false;
    }

    private boolean existeTelefono(String telefono) {
        for (Paciente p : listaPacientes) {
            if (p.telefono.equals(telefono)) {
                return true;
            }
        }
        return false;
    }

    private boolean existeContrasena(String contrasena) {
        for (Paciente p : listaPacientes) {
            if (p.contrasena.equals(contrasena)) {
                return true;
            }
        }
        return false;
    }
